static const int DB_SIZE = sizeof(db) / sizeof(db[0]);

static int ouiCmp(const uint8_t* a, const uint8_t* b) {
    for (int i = 0; i < 3; i++) {
        if (a[i] < b[i]) return -1;
        if (a[i] > b[i]) return  1;
    }
    return 0;
}

const char* ouiLookup(const uint8_t* mac) {
    if (!mac) return nullptr;
    int lo = 0, hi = DB_SIZE - 1;
    while (lo <= hi) {
        int mid = (lo + hi) / 2;
        int c = ouiCmp(mac, db[mid].prefix);
        if (c == 0) return db[mid].vendor;
        if (c < 0) hi = mid - 1;
        else        lo = mid + 1;
    }
    return nullptr;
}

int ouiCount() { return DB_SIZE; }
